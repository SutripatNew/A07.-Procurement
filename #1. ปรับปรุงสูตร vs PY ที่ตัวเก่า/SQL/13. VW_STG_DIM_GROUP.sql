CREATE VIEW [dbo].[VW_STG_DIM_GROUP] AS 

SELECT *
FROM
(
	SELECT
	SharePoint_List,
	Item_ID,Title,
	[NAME],
	NAME_DSP

	FROM  
	(
		SELECT SHAREPOINT_LIST,ITEM_ID,Title,QUESTION_NAME,RESPONSE FROM STG_DIM_GROUP
		WHERE QUESTION_NAME IN ('NAME','NAME_DSP')
	) AS F
	PIVOT  
	(  
	MIN(RESPONSE) FOR QUESTION_NAME IN ([NAME],[NAME_DSP])
	) AS P
) A 


GO


